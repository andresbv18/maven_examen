package maven.examen;

public class Poliza {
	
	
	public String nombre;
	public String descripcion;
	public int anyoVigencia;
	public String codigoPoliza;
	public int valorPoliza;
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public int getAnyoVigencia() {
		return anyoVigencia;
	}
	public void setAnyoVigencia(int anyoVigencia) {
		this.anyoVigencia = anyoVigencia;
	}
	public String getCodigoPoliza() {
		return codigoPoliza;
	}
	public void setCodigoPoliza(String codigoPoliza) {
		this.codigoPoliza = codigoPoliza;
	}
	public int getValorPoliza() {
		return valorPoliza;
	}
	public void setValorPoliza(int valorPoliza) {
		this.valorPoliza = valorPoliza;
	}
	
	

}
